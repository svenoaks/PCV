package de.tinysip.sip;

/**
 * The states of the sip request. Used by the SipManager.
 * 
 * @author Sebastian
 *
 */
public enum SipRequestState
{
	/**
	 * This state is used while the SipManager tries to register the SIP account with the provider.
	 */
	REGISTER, 
	/**
	 * This state is used while the SipManager tries to register the SIP account with the provider and authorization is required.
	 */
	AUTHORIZATION, 
	/**
	 * This state is used while the SipManager tries to unregister the SIP account from the provider.
	 */
	UNREGISTER, 
	/**
	 * This state is used while the SipManager tries to unregister the SIP account from the provider and authorization is required.
	 */
	UNREGISTER_AUTHORIZATION
}
